// nerdfont.h
// - 定义了Nerd Font的图标对应UTF-8编码
// - 字形参考https://www.nerdfonts.com/cheat-sheet

#pragma once

#define NF_FA_REGION0 0xED00, 0xEFD0
#define NF_FA_REGION1 0xF000, 0xF300
#define NF_MATERIAL_REGION 0xF0001, 0xF1AF0

// 警告
#define ICON_WARNING "\uf071"

// 播放
#define ICON_PLAY "\uf04b"

// 暂停
#define ICON_PAUSE "\uf28b"

// 停止
#define ICON_STOP "\uf04d"

// 导入
#define ICON_IMPORT "\uee38"

// 导出
#define ICON_EXPORT "\uee37"

// 撤销
#define ICON_UNDO "\uf0e2"

// 恢复
#define ICON_REDO "\uf01e"

// 复制
#define ICON_COPY "\uf0c5"

// 粘贴
#define ICON_PASTE "\uf0ea"

// X形（错误）
#define ICON_XMARK "\uf05c"

// Check（成功）
#define ICON_CHECK "\uf05d"

// 垃圾桶
#define ICON_TRASH "\uf014"

// 添加文件
#define ICON_FILE_ADD "\U000f0752"

// 外部链接
#define ICON_EXT_LINK "\uf08e"